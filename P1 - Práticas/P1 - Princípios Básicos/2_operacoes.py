# • Operações Aritméticas:
# - Adição | +
# - Subtração | -
# - Multiplicação | *
# - Divisão | /
# - Potencialização | **
# - Módulo (Resto da Divisão) | %

soma = 2+2
subtracao = 2-2
multiplicacao = 2*2
divisao = 2/2
potencializacao = 2**2
modulo = 2%2

print(f"Soma: {soma}")
print(f"Subtração: {subtracao}")
print(f"Multiplicação: {multiplicacao}")
print(f"Divisão: {divisao}")
print(f"Potencialização: {potencializacao}")
print(f"Módulo: {modulo}")